fruits = ["apple", "banana"]
print("Начальный список:", fruits)
# первоначальный список
fruits.append("orange")
print("После добавления 'orange':", fruits)
# добавила orange в конец списка
fruits.insert(1, "grape")
print("После вставки 'grape' по индексу 1:", fruits)
# вставила grape по инедексу 1
fruits.remove("banana")
print("После удаления 'banana':", fruits)
# удалила banana
fruits.sort()
print("После сортировки:", fruits)
# отсортировала список
fruits.reverse()
print("После переворота:", fruits)
# перевернула список
